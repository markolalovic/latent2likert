#' Agreeableness and Gender Data
#'
#' This dataset is a small part of `bfi` dataset in the `psychTools` package. 
#' It contains responses to the first 5 items of the agreeableness scale 
#' from the International Personality Item Pool (IPIP) and the gender attribute.
#' It includes data from 2800 subjects. Each item was answered on a six point 
#' Likert scale ranging from 1 (very inaccurate), to 6 (very accurate).
#' 
#' @docType data
#' 
#' @usage data(part_bfi)
#'
#' @format {An object of class \code{"data.frame"} with 2800 observations on the 
#' following 6 variables:}
#' \describe{
#'   \item{A1}{Am indifferent to the feelings of others.}
#'   \item{A2}{Inquire about others' well-being.}
#'   \item{A3}{Know how to comfort others.}
#'   \item{A4}{Love children.}
#'   \item{A5}{Make people feel at ease.}
#'   \item{gender}{Gender of the respondent (Male, Female).}
#' }
#' @source {International Personality Item Pool (ipip.ori.org)}
#' @references
#' Revelle, W. (2024). 
#' Psych: Procedures for Psychological, Psychometric, and Personality Research. 
#' \emph{Evanston, Illinois: Northwestern University.}
#' \url{https://CRAN.R-project.org/package=psych} 
